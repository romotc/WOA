package com.hongganju.node;

public class NodeConfig {
	private static String ip;
	private static String area;
	private static String isp;
	private static Integer clusterId;
	
	public static Integer getClusterId() {
		return clusterId;
	}
	public static void setClusterId(Integer clusterId) {
		NodeConfig.clusterId = clusterId;
	}
	public static String getIp() {
		return ip;
	}
	public static void setIp(String ip) {
		NodeConfig.ip = ip;
	}
	public static String getArea() {
		return area;
	}
	public static void setArea(String area) {
		NodeConfig.area = area;
	}
	public static String getIsp() {
		return isp;
	}
	public static void setIsp(String isp) {
		NodeConfig.isp = isp;
	}
	
}
