package com.hongganju.db.entity.base;

/**
 * AbstractDbmanager entity provides the base persistence definition of the
 * Dbmanager entity. @author MyEclipse Persistence Tools
 */

public abstract class AbstractDbmanager implements java.io.Serializable {

	// Fields

	private Short id;
	private String url;
	private String username;
	private Short customId;
	private String password;
	private String style;
	private Long capacity;
	private Short status;
	private Short dbtype;
	private String hbaseTableName;
	private String slaveUrl;
	private String slaveUsername;
	private String slavePassword;
	private Integer maxConnection;
	private Integer minConnection;
	// Constructors

	/** default constructor */
	public AbstractDbmanager() {
	}

	/** full constructor */
	public AbstractDbmanager(String url, String username, String password,
			String style, Long capacity, Short status) {
		this.url = url;
		this.username = username;
		this.password = password;
		this.style = style;
		this.capacity = capacity;
		this.status = status;
	}

	// Property accessors

	public Short getId() {
		return this.id;
	}

	public void setId(Short id) {
		this.id = id;
	}

	public String getUrl() {
		return this.url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getUsername() {
		return this.username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return this.password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getStyle() {
		return this.style;
	}

	public void setStyle(String style) {
		this.style = style;
	}

	public Long getCapacity() {
		return this.capacity;
	}

	public void setCapacity(Long capacity) {
		this.capacity = capacity;
	}

	public Short getStatus() {
		return this.status;
	}

	public void setStatus(Short status) {
		this.status = status;
	}

	public Short getDbtype() {
		return dbtype;
	}

	public void setDbtype(Short dbtype) {
		this.dbtype = dbtype;
	}

	public Short getCustomId() {
		return customId;
	}

	public void setCustomId(Short customId) {
		this.customId = customId;
	}

	public String getHbaseTableName() {
		return hbaseTableName;
	}

	public void setHbaseTableName(String hbaseTableName) {
		this.hbaseTableName = hbaseTableName;
	}

	public String getSlaveUrl() {
		return slaveUrl;
	}

	public void setSlaveUrl(String slaveUrl) {
		this.slaveUrl = slaveUrl;
	}

	public String getSlaveUsername() {
		return slaveUsername;
	}

	public void setSlaveUsername(String slaveUsername) {
		this.slaveUsername = slaveUsername;
	}

	public String getSlavePassword() {
		return slavePassword;
	}

	public void setSlavePassword(String slavePassword) {
		this.slavePassword = slavePassword;
	}

	public Integer getMaxConnection() {
		return maxConnection;
	}

	public void setMaxConnection(Integer maxConnection) {
		this.maxConnection = maxConnection;
	}

	public Integer getMinConnection() {
		return minConnection;
	}

	public void setMinConnection(Integer minConnection) {
		this.minConnection = minConnection;
	}

}