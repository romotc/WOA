package com.hongganju.db.entity.base;

/**
 * AbstractWoacluster entity provides the base persistence definition of the
 * Woacluster entity.
 * 
 * @author MyEclipse Persistence Tools
 */

public abstract class AbstractWoacluster implements java.io.Serializable {

	// Fields

	private Integer clusterId;
	private String areaId;
	private String ispid;

	// Constructors

	/** default constructor */
	public AbstractWoacluster() {
	}

	/** minimal constructor */
	public AbstractWoacluster(Integer clusterId) {
		this.clusterId = clusterId;
	}

	/** full constructor */
	public AbstractWoacluster(Integer clusterId, String areaId, String ispid) {
		this.clusterId = clusterId;
		this.areaId = areaId;
		this.ispid = ispid;
	}

	// Property accessors

	public Integer getClusterId() {
		return this.clusterId;
	}

	public void setClusterId(Integer clusterId) {
		this.clusterId = clusterId;
	}

	public String getAreaId() {
		return this.areaId;
	}

	public void setAreaId(String areaId) {
		this.areaId = areaId;
	}

	public String getIspid() {
		return this.ispid;
	}

	public void setIspid(String ispid) {
		this.ispid = ispid;
	}

}