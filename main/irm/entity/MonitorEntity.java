package irm.entity;

/**
 * 检测目标分析实体
 * @author	TangDican
 * @date	2013 Aug 31, 2013
 * @email	tangdican2008@163.com
 */
public class MonitorEntity {
	// 区域id
	private String areaid;
	// 网站
	private String site;
	// 域名数量
	private Integer domain_cnt;
	// 本省引入率
	private float province_in_rate;
	// 本省引入深度
	private float province_resource_service_rate;
	// 运营商id
	private String ispid;
	
	/**
	 * @return the areaid
	 */
	public String getAreaid() {
		return areaid;
	}
	/**
	 * @param areaid the areaid to set
	 */
	public void setAreaid(String areaid) {
		this.areaid = areaid;
	}
	/**
	 * @return the site
	 */
	public String getSite() {
		return site;
	}
	/**
	 * @param site the site to set
	 */
	public void setSite(String site) {
		this.site = site;
	}
	/**
	 * @return the domain_cnt
	 */
	public Integer getDomain_cnt() {
		return domain_cnt;
	}
	/**
	 * @param domain_cnt the domain_cnt to set
	 */
	public void setDomain_cnt(Integer domain_cnt) {
		this.domain_cnt = domain_cnt;
	}
	/**
	 * @return the province_in_rate
	 */
	public float getProvince_in_rate() {
		return province_in_rate;
	}
	/**
	 * @param province_in_rate the province_in_rate to set
	 */
	public void setProvince_in_rate(float province_in_rate) {
		this.province_in_rate = province_in_rate;
	}
	/**
	 * @return the province_resource_service_rate
	 */
	public float getProvince_resource_service_rate() {
		return province_resource_service_rate;
	}
	/**
	 * @param province_resource_service_rate the province_resource_service_rate to set
	 */
	public void setProvince_resource_service_rate(
			float province_resource_service_rate) {
		this.province_resource_service_rate = province_resource_service_rate;
	}
	/**
	 * @return the ispid
	 */
	public String getIspid() {
		return ispid;
	}
	/**
	 * @param ispid the ispid to set
	 */
	public void setIspid(String ispid) {
		this.ispid = ispid;
	}
}
