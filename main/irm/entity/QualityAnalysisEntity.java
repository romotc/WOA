	private float downloadTime;
	// 下载速度
	private float downloadSpeed;
	// 成功次数
	private int successNum;
	// 域名等级
	private String domainLevel;
	
	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	/**
	 * @return the domain
	 */
	public String getDomain() {
		return domain;
	}
	/**
	 * @param domain the domain to set
	 */
	public void setDomain(String domain) {
		this.domain = domain;
	}
	/**
	 * @return the serverIp
	 */
	public String getServerIp() {
		return serverIp;
	}
	/**
	 * @param serverIp the serverIp to set
	 */
	public void setServerIp(String serverIp) {
		this.serverIp = serverIp;
	}
	/**
	 * @return the nodeAreaId
	 */
	public String getServerAreaId() {
		return serverAreaId;
	}
	/**
	 * @param nodeAreaId the nodeAreaId to set
	 */
	public void setServerAreaId(String serverAreaId) {
		this.serverAreaId = serverAreaId;
	}
