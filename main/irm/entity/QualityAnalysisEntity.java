	private Integer id;
	// 域名
	private String domain;
	// 服务端ip
	private String serverIp;
	// 区域id
	private String serverAreaId;
	// 运营商id
	private String serverISPId;
	// dns解析时间
	private float dnsResolveTotalTime;
	// 连接时间
	private float ConnectionTime;
	// 首字节时间
	private float FirstByteTime;
